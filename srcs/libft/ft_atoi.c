#include "../../includes/libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	isminus;
	int	result;

	i = 0;
	isminus = 1;
	result = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			isminus = -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		result = (result * 10) + (str[i] - 48);
		i++;
	}
	return (result * isminus);
}
